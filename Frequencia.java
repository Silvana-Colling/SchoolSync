public class Frequencia {
    private int numeroFaltas;

    // Construtor
    public Frequencia(int numeroFaltas) {
        this.numeroFaltas = numeroFaltas;
    }

    // Getter e Setter
    public int getNumeroFaltas() {
        return numeroFaltas;
    }

    public void setNumeroFaltas(int numeroFaltas) {
        this.numeroFaltas = numeroFaltas;
    }
}